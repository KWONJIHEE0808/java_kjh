package day2;

public class Ex5_Comparison {

	public static void main(String[] args) {
		/* >=을 반대로 쓰면 안됨 (ex. =>, =<)
		 * ==을 = 과 혼동하지말것
		 * 결과값은 true or false
		 */
		int score = 95;
		System.out.println(score >= 90); //true
		System.out.println(score <= 95); //true
		System.out.println(score == 95); //true
		System.out.println(score != 95); //false
	
		

	}

}
